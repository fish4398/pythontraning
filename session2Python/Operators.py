# Arithmetic operators
a = 5
b = 2

print(a+b)
print(a - b)
print(a * b)

print(a/b)
print(a//b)
print(a%b)
print(a**b)  # 5 ** 2 = 25

# relational operators/comparison operators
# > < >= <= != ==
# relational operators always return boolean value True / False
a = 5
b = 10

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

# logical operators
# and or not
# always return boolean value True / False
a = True
b = False

print(a and b)
print(a or b)
print(not a)